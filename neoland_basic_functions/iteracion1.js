// Iteración #1: Buscar el máximo: Completa la función que tomando dos números como argumento devuelva el más alto

function sum(num1, num2) {
    if (num1 < num2) {
        return num2
    } else {
        return num1
    }
}
console.log(sum(30, 2))



function suma(numero1, numero2) {
    if (numero1 > numero2) {
        return numero1
    } else {
        return numero2
    }
}
console.log(suma(500, 200))